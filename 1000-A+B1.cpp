#include <stdio.h>

/*题目描述
你的任务是计算a+b。这是为了acm初学者专门设计的题目。你肯定发现还有其他题目跟这道题的标题类似，这些问题也都是专门为初学者提供的。

输入
输入包含一系列的a和b对，通过空格隔开。一对a和b占一行。

输出
对于输入的每对a和b，你需要依次输出a、b的和。

如对于输入中的第二对a和b，在输出中它们的和应该也在第二行。

样例输入
1 5
10 20
样例输出
6
30*/
int main(){
    int a,b;
    while(scanf("%d %d",&a,&b)){
        printf("%d\n",a+b);
    }
    return 0;
}
